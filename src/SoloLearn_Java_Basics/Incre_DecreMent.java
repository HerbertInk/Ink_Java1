public class Incre_DecreMent {
    public static void main(String[]arg){
        int ink = 20; ++ink;  /*21*/ System.out.println(ink);
        int bit = 45; /*44*/ int b = --bit; System.out.println(b);
        int bby = 1901; /*1902*/ int a = bby++; System.out.println(bby);
        int bin = 12; /*11*/ int j = bin--; System.out.println(bin);
    }
}
